									<p><b>Protetica dentară (reconstrucția danturii)</b> este o ramură importantă în stomatologie care se ocupă cu reconstrucția danturii prin diverse lucrări protetice. Atunci când, din diverse motive, din dinte lipsește o porțiune ce nu poate fi refăcută prin obturație (plombă), sau pereții dentari sunt prea subțiri pentru a suporta presiunile masticatorii, e necesară acoperirea dintelui cu o coroană.</p>
									
									<p> De asemenea, când lipsește un dinte de pe arcadă și spațiul gol e mărginit de dinți ce nu sunt integri, sunt deteriorați, cariați sau au obturații coronare (plombe), se recurge la o punte. Se îmbracă dinții limitrofi și se înlocuiește spațiul edentat cu o coroană care să refacă fizionomia și funcționalitatea pierdute.</p>
									
									<br>
								<div	style="color: blue; font-weight: normal;font-size: 22px"> <b> Cele mai frecvente tratamente protetice sunt:</b>	
													
</div>
