<h1><span>Ortodonție</span></h1>
									<br>
									<br>
									<p><b>Ortodonția</b>  este ramura stomatologiei care se ocupă de prevenirea și tratamentul malocluziei. Termenul „ortodonție” înseamnă „dinți drepți”, în timp ce „malocluzie” înseamnă „relația necorespunzătoare de contact între dinții arcadei superioare și cei ai arcadei inferioare”.</p>
									<br>
									<p>În timpul copilăriei și adolescenței, oasele maxilare cresc și se maturizează, de aceea este mai ușor de modificat poziția dinților în această etapă. În cazul adulților tratamentul poate să dureze mai mult, pentru că oasele maxilare și-au întrerupt creșterea.</p>
									<p>Tratamentul este recomandat deoarece pe termen lung afecțiunile netratate pot duce la apariția unor defecte de vorbire, precum și la formarea tartrului și implicit a cariilor.</p>
									<p>Înghesuirile și ocluziile incorecte dentare afectează în timp sănătatea dinților, a gingiilor și a articulației temporo-mandibulare. O masticație deficitară poate avea urmări asupra integrității tubului digestiv.
